export const ListTagIdSignalR = (lineId, stationId, arrayTags) => {
    return arrayTags.map((res) => `${lineId}/${stationId}/${res}`)
}

export const ListTagIdSignalRLineMachine = (lineId, arrayTags) => {
    return arrayTags.flatMap((res) => [
        `${lineId}/${res}/machineStatus`,
        `${lineId}/${res}/productivity`,
        // `${lineId}/${res}/error`,
        // `${lineId}/${res}/endError`,
    ])
}

export const ListTagIdDashboardSignalR = (arrayLineId) => {
    return arrayLineId.flatMap((res) => [
        `${res.lineId}/goodProduct`,
        // `${res.lineId}/errorProduct`,
        `${res.lineId}/productCount`,
        `${res.lineId}/machineStatus`,
    ])
}

